
public class Variables {
    public static void main(String[] args) {

        // String = holds text
        String firstName = "Jhon";
        String lastName = "Smith";
        System.out.println(firstName + " " + lastName);

        // int = holds whole numbers
        int age = 30;
        int yearOfBorn = 1991;

        System.out.println(age);

        System.out.println(firstName + " " + lastName + " " + age);

        System.out.println(firstName + "'s year of birth is " + yearOfBorn);


        // float = holds decimals
        float tomatoPrice = 1.49f;
        float bodyTemperature = 97.5f;

        System.out.println("Tomato price is $" + tomatoPrice);
        System.out.println("My body temperature is " + bodyTemperature + "F ");


        // char = holds characters = a A B

        char symbol = 'F';
        System.out.println("My body temperature is " + bodyTemperature + symbol);

        char sign = '$';
        float price = 10;

        System.out.println(sign + "" + price);

        // boolean = true & false

        boolean isSnowing = false;
        System.out.println(isSnowing);







    }
}
